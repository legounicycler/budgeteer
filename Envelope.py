class Envelope:

    def __init__(self, name, balance, budget):
        self.name = name
        self.balance = balance
        self.budget = budget

    def get_name():
        return self.name

    def set_name(name):
        self.name = name

    def get_balance():
        return self.balance

    def set_balance(balance):
        self.balance = balance

    def get_budget():
        return self.budget

    def set_budget(budget):
        self.budget = budget
